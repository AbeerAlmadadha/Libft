#include	"libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	c = '\0';
	if (n < 0)
	{
		if (n == -2147483648)
		{
			write(fd, "-2147483648", 11);
			return ;
		}
		write(fd, "-", 1);
		n = -n;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		n = n % 10;
	}
	c = n + '0';
	write(fd, &c, 1);
}
